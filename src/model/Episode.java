package model;

public class Episode {
    private String Premiere;
    private int length;

    public String getPremiere() {
        return Premiere;
    }

    public void setPremiere(String premiere) {
        Premiere = premiere;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }
}
